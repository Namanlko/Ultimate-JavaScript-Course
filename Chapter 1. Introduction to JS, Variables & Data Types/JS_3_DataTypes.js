// 3) Data Types in JavaScript.

// 3.A)
var a = null;
console.log(a);
console.log(typeof a)

// 3.B)
var b = Number(10);
console.log(b);
console.log(typeof b)

// 3.C)
var c = String("Naman Pandey");
console.log(c);
console.log(typeof c)

// 3.D)
var d = Symbol("foo");
console.log(d);
console.log(typeof d)

// 3.E)
var e = true;
console.log(e);
console.log(typeof e)

// 3.F)
var f = BigInt(125678934);
console.log(f);
console.log(typeof f)

// 3.G)
var g = undefined;
console.log(g);
console.log(typeof g)
